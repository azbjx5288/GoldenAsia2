package com.goldenasia.lottery.material;

import com.goldenasia.lottery.data.Lottery;

/**
 * Created on 2016/01/13.
 * @author ACE
 * @功能描述: 包装开奖周期信息
 *
 */
public class PeriodInfo {

    private RecordTime salesTime;   //销售时间
    private Lottery lottery;     //当前彩种名称
    private String openCode="";        //开奖号
    private String salesIssue="";           //奖期信息
    private String openIssue="";
    public PeriodInfo() {
    }

    public RecordTime getSalesTime() {
        return salesTime;
    }

    public void setSalesTime(RecordTime salesTime) {
        this.salesTime = salesTime;
    }

    public String getOpenCode() {
        return openCode;
    }

    public void setOpenCode(String openCode) {
        this.openCode = openCode;
    }

    public Lottery getLottery() {
        return lottery;
    }

    public void setLottery(Lottery lottery) {
        this.lottery = lottery;
    }

    public String getSalesIssue() {
        return salesIssue;
    }

    public void setSalesIssue(String salesIssue) {
        this.salesIssue = salesIssue;
    }

    public String getOpenIssue() {
        return openIssue;
    }

    public void setOpenIssue(String openIssue) {
        this.openIssue = openIssue;
    }
}
