package com.goldenasia.lottery.data;

import com.android.volley.Request;
import com.goldenasia.lottery.base.net.RequestConfig;

/**
 * Created by ACE-PC on 2016/5/6.
 */
@RequestConfig(api = "?c=user&a=bindCard", response = String.class)
public class BindCardCommand {
    private int bind_card_id;
    private String bind_card_num;
    private String province;
    private String city;
    private String branch_name;
    private String secpwd;

    public int getBind_card_id() {
        return bind_card_id;
    }

    public void setBind_card_id(int bind_card_id) {
        this.bind_card_id = bind_card_id;
    }

    public String getBind_card_num() {
        return bind_card_num;
    }

    public void setBind_card_num(String bind_card_num) {
        this.bind_card_num = bind_card_num;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getBranch_name() {
        return branch_name;
    }

    public void setBranch_name(String branch_name) {
        this.branch_name = branch_name;
    }

    public String getSecpwd() {
        return secpwd;
    }

    public void setSecpwd(String secpwd) {
        this.secpwd = secpwd;
    }
}
