package com.goldenasia.lottery.data;

import android.util.SparseIntArray;

import java.io.Serializable;

public class Additional implements Serializable {
    private SparseIntArray multipleArray;
    private boolean stop_on_win = true;

    public SparseIntArray getMultipleArray() {
        return multipleArray;
    }

    public void setMultipleArray(SparseIntArray multipleArray) {
        this.multipleArray = multipleArray;
    }

    public boolean isStopOnWin() {
        return stop_on_win;
    }

    public void setStopOnWin(boolean stop_on_win) {
        this.stop_on_win = stop_on_win;
    }
}
