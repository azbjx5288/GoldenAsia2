package com.goldenasia.lottery.data;

import com.google.gson.annotations.SerializedName;

import java.util.ArrayList;

/**
 * Created by Sakura on 2016/9/30.
 * 刮刮彩卡片列表返回信息
 */

public class ScrapeTypeEntity
{
    @SerializedName("scrape_type")
    private int scrapeType;
    @SerializedName("scrape_types")
    private ArrayList<ScrapeType> scrapeTypeList;
    @SerializedName("packages")
    private ArrayList<GgcPackageInfo> ggcPackageEntities;

    public int getScrapeType()
    {
        return scrapeType;
    }

    public void setScrapeType(int scrapeType)
    {
        this.scrapeType = scrapeType;
    }

    public ArrayList<ScrapeType> getScrapeTypeList()
    {
        return scrapeTypeList;
    }

    public void setScrapeTypeList(ArrayList<ScrapeType> scrapeTypeList)
    {
        this.scrapeTypeList = scrapeTypeList;
    }

    public ArrayList<GgcPackageInfo> getGgcPackageEntities()
    {
        return ggcPackageEntities;
    }

    public void setGgcPackageEntities(ArrayList<GgcPackageInfo> ggcPackageEntities)
    {
        this.ggcPackageEntities = ggcPackageEntities;
    }
}
