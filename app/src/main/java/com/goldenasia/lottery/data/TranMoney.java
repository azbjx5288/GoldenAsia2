package com.goldenasia.lottery.data;

import com.google.gson.annotations.SerializedName;

/**
 * Created by ACE-PC on 2017/1/20.
 */

public class TranMoney {
    /**
     * jyz_balance : 1000
     * pt_balance : 1000
     * jc_balance : 1000
     * ga_balance : 1000
     */

    @SerializedName("jyz_balance")
    private String jyzBalance;
    @SerializedName("pt_balance")
    private String ptBalance;
    @SerializedName("jc_balance")
    private String jcBalance;
    @SerializedName("ga_balance")
    private String gaBalance;

    public String getJyzBalance() {
        return jyzBalance;
    }

    public void setJyzBalance(String jyzBalance) {
        this.jyzBalance = jyzBalance;
    }

    public String getPtBalance() {
        return ptBalance;
    }

    public void setPtBalance(String ptBalance) {
        this.ptBalance = ptBalance;
    }

    public String getJcBalance() {
        return jcBalance;
    }

    public void setJcBalance(String jcBalance) {
        this.jcBalance = jcBalance;
    }

    public String getGaBalance() {
        return gaBalance;
    }

    public void setGaBalance(String gaBalance) {
        this.gaBalance = gaBalance;
    }
}
